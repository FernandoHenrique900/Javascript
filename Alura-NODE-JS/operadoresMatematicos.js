//Operadores matematicos

const num1 = 10
const num2 = 5

console.log('Variavel-1:', num1)
console.log('Variavel-2:', num2)

console.log('Resultados:')

console.log('Subtração:', num1 - num2) //resultado: 5, pq 10 menos 5 é 5.
console.log('Multiplicação:', num1 * num2) //resultado: 50, pq 10 multiplicado por 5 é 50.
console.log('Divisão:', num1 / num2) //resultado: 2, pq 10 dividido por 5 é 2.
console.log('Módulo:', num1 % num2) //resultado: 0, pq 10 é divisível por 5 sem resto.
console.log('Soma:', num1 + num2) //resultado: 15, pq 10 mais 5 é 15.s
console.log('Incremento:', num1++) // erro pq é uma variavel constante.


