/*Exemplo de Laco While*/
/*
    O laco while executa o bloco de código enquanto a condição for verdadeira.
    A condição é verificada no início do loop.
*/
let contador = 1

while(contador <=10){
    console.log('Numero atual (while):',contador)
    contador++
}

/*Exemplo de Laco Do While*/
/*
    O laco do while executa o bloco de código pelo menos uma vez,
    independentemente da condição ser verdadeira ou falsa.
    A condição é verificada no final do loop.
*/
let numero = 1

do{
    console.log('Numero atual (do while):',numero)
    numero++
} while(numero <=10)



