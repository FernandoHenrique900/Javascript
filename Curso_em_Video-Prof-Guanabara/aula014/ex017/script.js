function tabuada(){
    var num = document.getElementById('txtn')
    var tab = document.getElementById('seltab')
    if(num.value.length == 0){
        window.alert('Por favor, digite um número!')
    }
    else{
        var n = Number(num.value)
        var c = 1
        tab.innerHTML = '' // toda vez q for carregar zerar o select, se não só add
        while(c <= 10){
            var item = document.createElement('option')
            item.text = `${n} x ${c} = ${n*c}`
            item.value = `tab${c}` // P/ Javascrip não importa muito, porém para PHP(SERVERSIDE AJUDA)
            tab.appendChild(item) //filha de item pra mostrar o valor
            c++
        }
    }
}